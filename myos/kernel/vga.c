// 在图形模式下绘制文本
void vga_draw_text(int x, int y, const char* text, uint8_t color) {
    if (current_mode != VGA_MODE_GRAPHIC) {
        return;
    }
    
    int cursor_x = x;
    int cursor_y = y;
    
    for (int i = 0; text[i] != '\0'; i++) {
        char c = text[i];
        
        // 处理换行符
        if (c == '\n') {
            cursor_x = x;
            cursor_y += 8;  // 换到下一行
            continue;
        }
        
        // 检查字符是否在有效范围内
        if (c < 0 || c > 127) {
            c = '?';  // 替换无效字符
        }
        
        // 绘制单个字符
        for (int row = 0; row < 7; row++) {
            for (int col = 0; col < 5; col++) {
                if (font_5x7[c][col] & (1 << row)) {
                    vga_plot_pixel(cursor_x + col, cursor_y + row, color);
                }
            }
        }
        
        // 移动光标到下一个字符位置
        cursor_x += 6;  // 5像素字体宽度 + 1像素间距
        
        // 检查是否需要换行
        if (cursor_x > 320 - 6) {
            cursor_x = x;
            cursor_y += 8;  // 7像素字体高度 + 1像素间距
        }
    }
} 