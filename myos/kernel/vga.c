// 外部函数引用
extern void outb(unsigned short port, unsigned char data);
extern unsigned char inb(unsigned short port);

// VGA寄存器定义
#define VGA_AC_INDEX      0x3C0
#define VGA_AC_WRITE      0x3C0
#define VGA_AC_READ       0x3C1
#define VGA_MISC_WRITE    0x3C2
#define VGA_SEQ_INDEX     0x3C4
#define VGA_SEQ_DATA      0x3C5
#define VGA_DAC_READ_INDEX 0x3C7
#define VGA_DAC_WRITE_INDEX 0x3C8
#define VGA_DAC_DATA      0x3C9
#define VGA_MISC_READ     0x3CC
#define VGA_GC_INDEX      0x3CE
#define VGA_GC_DATA       0x3CF
#define VGA_CRTC_INDEX    0x3D4
#define VGA_CRTC_DATA     0x3D5
#define VGA_INSTAT_READ   0x3DA

// VGA内存地址
#define VGA_TEXT_MEMORY   0xB8000
#define VGA_GRAPHIC_MEMORY 0xA0000

// 当前VGA模式
static uint8_t current_mode = VGA_MODE_TEXT;

// 初始化VGA
void vga_init() {
    // 默认设置为文本模式
    vga_set_mode(VGA_MODE_TEXT);
}

// 设置VGA模式
void vga_set_mode(uint8_t mode) {
    outb(0x3C2, 0x63); // 杂项输出寄存器
    
    if (mode == VGA_MODE_GRAPHIC) {
        // 设置BIOS的int 0x10来切换到图形模式 (320x200 256色)
        __asm__ volatile (
            "mov $0x13, %%ax\n"
            "int $0x10"
            ::: "ax"
        );
        current_mode = VGA_MODE_GRAPHIC;
    } else {
        // 设置BIOS的int 0x10来切换到文本模式 (80x25)
        __asm__ volatile (
            "mov $0x03, %%ax\n"
            "int $0x10"
            ::: "ax"
        );
        current_mode = VGA_MODE_TEXT;
    }
}

// 清屏
void vga_clear_screen(uint8_t color) {
    if (current_mode == VGA_MODE_GRAPHIC) {
        // 图形模式 - 填充整个显存
        unsigned char* vga_memory = (unsigned char*)VGA_GRAPHIC_MEMORY;
        memset(vga_memory, color, 320 * 200);
    } else {
        // 文本模式 - 用空格和属性填充
        unsigned short* vga_memory = (unsigned short*)VGA_TEXT_MEMORY;
        for (int i = 0; i < 80 * 25; i++) {
            vga_memory[i] = (color << 8) | ' ';
        }
    }
}

// 在图形模式下画一个像素点
void vga_plot_pixel(int x, int y, uint8_t color) {
    if (current_mode != VGA_MODE_GRAPHIC || x < 0 || x >= 320 || y < 0 || y >= 200) {
        return;
    }
    
    unsigned char* vga_memory = (unsigned char*)VGA_GRAPHIC_MEMORY;
    vga_memory[y * 320 + x] = color;
}

// 绘制直线 (Bresenham算法)
void vga_draw_line(int x1, int y1, int x2, int y2, uint8_t color) {
    int dx = abs(x2 - x1);
    int dy = abs(y2 - y1);
    int sx = (x1 < x2) ? 1 : -1;
    int sy = (y1 < y2) ? 1 : -1;
    int err = dx - dy;
    
    while (1) {
        vga_plot_pixel(x1, y1, color);
        
        if (x1 == x2 && y1 == y2) {
            break;
        }
        
        int e2 = 2 * err;
        if (e2 > -dy) {
            err -= dy;
            x1 += sx;
        }
        if (e2 < dx) {
            err += dx;
            y1 += sy;
        }
    }
}

// 绘制矩形边框
void vga_draw_rectangle(int x, int y, int width, int height, uint8_t color) {
    vga_draw_line(x, y, x + width - 1, y, color);              // 上边
    vga_draw_line(x, y + height - 1, x + width - 1, y + height - 1, color); // 下边
    vga_draw_line(x, y, x, y + height - 1, color);             // 左边
    vga_draw_line(x + width - 1, y, x + width - 1, y + height - 1, color);  // 右边
}

// 绘制填充矩形
void vga_fill_rectangle(int x, int y, int width, int height, uint8_t color) {
    for (int i = 0; i < height; i++) {
        for (int j = 0; j < width; j++) {
            vga_plot_pixel(x + j, y + i, color);
        }
    }
}

// 绘制圆形 (Bresenham中点画圆法)
void vga_draw_circle(int x0, int y0, int radius, uint8_t color) {
    int x = radius;
    int y = 0;
    int err = 0;
    
    while (x >= y) {
        vga_plot_pixel(x0 + x, y0 + y, color);
        vga_plot_pixel(x0 + y, y0 + x, color);
        vga_plot_pixel(x0 - y, y0 + x, color);
        vga_plot_pixel(x0 - x, y0 + y, color);
        vga_plot_pixel(x0 - x, y0 - y, color);
        vga_plot_pixel(x0 - y, y0 - x, color);
        vga_plot_pixel(x0 + y, y0 - x, color);
        vga_plot_pixel(x0 + x, y0 - y, color);
        
        if (err <= 0) {
            y += 1;
            err += 2*y + 1;
        }
        
        if (err > 0) {
            x -= 1;
            err -= 2*x + 1;
        }
    }
}

